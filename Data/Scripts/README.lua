The Bartending component allows players to pick up a pint, fill it with beer from barrels, and serve
it for other players to drink.
-----------------------------------------------------------------------------------------------------

==========
How to use
==========

Drag the Bartending template into the Hierarchy. Enter preview mode and pickup a cup from the bar. Go
to the barrels to fill up the up. You can now drink the pint, or place it on the bar to serve it to
other players to drink.

Inside the template in the scripts folder, there is a Pint script that has custom properties on it to
modify the name, amount of uses, and the template used for the cup.

This component was extracted from the LootMMO Open Source project.

